#!/usr/bin/env python3
def to_str(n: float) -> str:
    """
    Returns the string representation of a given float.

    Parameters:
      - n (float): The float for which the string representation is needed.

    Returns:
      - str: The string representation of the input float.

    Example:
      >>> to_str(3.14)
      '3.14'
    """
    return str(n)
