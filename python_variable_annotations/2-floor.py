#!/usr/bin/env python3
import math

def floor(n: float) -> int:
    """
    Returns the floor of a given float.

    Parameters:
      - n (float): The float for which the floor needs to be calculated.

    Returns:
      - int: The floor of the input float.

    Example:
      >>> floor(3.14)
      3
    """
    return math.floor(n)
