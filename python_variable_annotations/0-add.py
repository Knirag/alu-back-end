#!/usr/bin/env python3
def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns their sum.

    Parameters:
      - a (float): The first number.
      - b (float): The second number.

    Returns:
      - float: The sum of a and b.
       Example:
      >>> add(1.11, 2.22)
      3.33
    """
    return a + b
